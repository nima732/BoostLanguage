package com.example.boostlanguage.entity;

public class Setting {

	private long id;
	private float numberCorrectDay;
	private float numberWrongDay;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public float getNumberCorrectDay() {
		return numberCorrectDay;
	}
	public void setNumberCorrectDay(float numberCorrectDay) {
		this.numberCorrectDay = numberCorrectDay;
	}
	public float getNumberWrongDay() {
		return numberWrongDay;
	}
	public void setNumberWrongDay(float numberWrongDay) {
		this.numberWrongDay = numberWrongDay;
	}

	

	
	
}
